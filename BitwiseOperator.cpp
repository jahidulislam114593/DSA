#include<bits/stdc++.h>
using namespace std;

int main()
{
	cout<<(6 & 10)<<endl; // 0 in decimal (Bitwise &)
	cout<<(6 | 10)<<endl; //12 in decimal (Bitwise |)
	cout<<(6 ^ 10)<<endl; //12 in decimal (Bitwise ^ xor)
	cout<<(10 << 2)<<endl; //40 in decimal (Bitwise <<)
	cout<<(10 >> 1)<<endl; //40 in decimal (Bitwise >>)
}